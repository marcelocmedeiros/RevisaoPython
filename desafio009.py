'''
Faça um programa que leia uma número inteiro e mostre sua tabuada. 
'''
print('='*30)
print('{:*^30}'.format('  TABUADA  '))
print('='*30)

num = int(input('Informe um número:'))
print('{} X 1 = {}\n'.format(num, num*1))
print('{} X 2 = {}\n'.format(num, num*2))
print('{} X 3 = {}\n'.format(num, num*3))
print('{} X 4 = {}\n'.format(num, num*4))
print('{} X 5 = {}\n'.format(num, num*5))